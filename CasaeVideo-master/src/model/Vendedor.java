package model;

public class Vendedor {

	private String cpf;
	private String carteiraTrabalho;
	private String pis;
	private String dependentes;
	private String dataNascimento;
	private String nome;
	private String endereço;
	private String telefone;
	private String formacaoProfissional;
	private String experienciasAnteriores;
	
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getCarteiraTrabalho() {
		return carteiraTrabalho;
	}
	public void setCarteiraTrabalho(String carteiraTrabalho) {
		this.carteiraTrabalho = carteiraTrabalho;
	}
	public String getPis() {
		return pis;
	}
	public void setPis(String pis) {
		this.pis = pis;
	}
	public String getDependentes() {
		return dependentes;
	}
	public void setDependentes(String dependentes) {
		this.dependentes = dependentes;
	}
	public String getDataNascimento() {
		return dataNascimento;
	}
	public void setDataNascimento(String dataNascimento) {
		this.dataNascimento = dataNascimento;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEndereço() {
		return endereço;
	}
	public void setEndereço(String endereço) {
		this.endereço = endereço;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getFormacaoProfissional() {
		return formacaoProfissional;
	}
	public void setFormacaoProfissional(String formacaoProfissional) {
		this.formacaoProfissional = formacaoProfissional;
	}
	public String getExperienciasAnteriores() {
		return experienciasAnteriores;
	}
	public void setExperienciasAnteriores(String experienciasAnteriores) {
		this.experienciasAnteriores = experienciasAnteriores;
	}
	
}
